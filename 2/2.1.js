function describeCountry(country, population, capitalCity) {
    return (country + " has " + population + " people and its capital city is " + capitalCity);
}

let place1 = describeCountry("place1", 80, "capital1");
let place2 = describeCountry("place2", 100, "capital2");
let place3 = describeCountry("place3", 30, "capital3");
console.log(place1);
console.log(place2);
console.log(place3);
